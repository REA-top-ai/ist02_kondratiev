def _range_group(value: int, ranges: list[tuple[int, int]]) -> tuple[int, int] | None:
    """Возвращает (lo, hi) диапазон, в который попадает value, иначе None."""
    for lo, hi in ranges:
        if lo <= value <= hi:
            return (lo, hi)
    return None


def _normalize_car_name(s: str) -> str:
    t = " ".join(s.strip().lower().split())
    # Разрешим несколько вариантов ввода
    if t in {"volkswagen polo", "vw polo", "polo", "volkswagen"}:
        return "polo"
    if t in {"bmw x1", "x1", "bmw"}:
        return "bmw_x1"
    return t


def task4_yandex_drive():
    # Таблица тарифов (как в задании)
    # Ключ: (car, age_range, exp_range, rep_range, jams_range) -> tariff_per_min
    #
    # Примечание: в PDF в одной строке BMW указано "119" (без запятой),
    # это выбивается из всех значений (10–12) и выглядит как опечатка "11,9".
    # Поэтому ниже используем 11.9.
    tariffs = {
        # Volkswagen Polo
        ("polo", (20, 27), (2, 9), (1, 2), (1, 3)): 8.0,
        ("polo", (20, 27), (2, 9), (1, 2), (4, 7)): 8.5,
        ("polo", (20, 27), (2, 9), (3, 5), (1, 3)): 7.5,
        ("polo", (20, 27), (2, 9), (3, 5), (4, 7)): 7.4,

        ("polo", (27, 34), (2, 9), (1, 2), (1, 3)): 7.2,
        ("polo", (27, 34), (2, 9), (3, 5), (1, 3)): 7.0,
        ("polo", (27, 34), (2, 9), (3, 5), (4, 7)): 7.2,
        ("polo", (27, 34), (10, 15), (1, 2), (1, 3)): 6.9,
        ("polo", (27, 34), (10, 15), (3, 5), (4, 7)): 6.6,
        ("polo", (27, 34), (10, 15), (1, 2), (4, 7)): 6.7,

        # BMW X1
        ("bmw_x1", (20, 27), (2, 9), (1, 2), (1, 3)): 12.0,
        ("bmw_x1", (20, 27), (2, 9), (1, 2), (4, 7)): 12.5,
        ("bmw_x1", (20, 27), (2, 9), (3, 5), (1, 3)): 11.6,
        ("bmw_x1", (20, 27), (2, 9), (3, 5), (4, 7)): 11.3,

        ("bmw_x1", (27, 34), (2, 9), (1, 2), (1, 3)): 11.4,
        ("bmw_x1", (27, 34), (2, 9), (3, 5), (1, 3)): 11.7,
        ("bmw_x1", (27, 34), (2, 9), (3, 5), (4, 7)): 11.9,  # в PDF "119"
        ("bmw_x1", (27, 34), (10, 15), (1, 2), (1, 3)): 10.8,
        ("bmw_x1", (27, 34), (10, 15), (3, 5), (4, 7)): 10.9,
        ("bmw_x1", (27, 34), (10, 15), (1, 2), (4, 7)): 11.0,
    }

    car_raw = input("Введите марку (Volkswagen Polo / BMW X1): ")
    car = _normalize_car_name(car_raw)

    age = parse_int(input("Введите возраст водителя: "))
    exp = parse_int(input("Введите стаж (в годах): "))
    rep = parse_int(input("Введите репутацию (коэффициент): "))
    jams = parse_int(input("Введите пробки (балл): "))
    duration = parse_int(input("Введите длительность поездки (в минутах): "))

    # Группировки строго по диапазонам из таблицы
    age_group = _range_group(age, [(20, 27), (27, 34)])
    exp_group = _range_group(exp, [(2, 9), (10, 15)])
    rep_group = _range_group(rep, [(1, 2), (3, 5)])
    jams_group = _range_group(jams, [(1, 3), (4, 7)])

    if not all([age_group, exp_group, rep_group, jams_group]):
        print("Нет тарифа: одно или несколько значений вне диапазонов таблицы.")
        return

    key = (car, age_group, exp_group, rep_group, jams_group)
    tariff = tariffs.get(key)

    if tariff is None:
        print("В таблице нет тарифа для такой комбинации параметров.")
        return

    price = duration * tariff
    # Можно округлить до 2 знаков, но не обязательно
    print(f"Стоимость вашей поездки составит {price}")


