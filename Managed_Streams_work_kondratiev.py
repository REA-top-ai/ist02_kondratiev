def task2_outliers():
    # Вводим max, mean, min, std и печатаем одно из сообщений по условиям
    max_value = parse_float(input("Введите максимум: "))
    mean_value = parse_float(input("Введите среднее: "))
    min_value = parse_float(input("Введите минимум: "))
    std_value = parse_float(input("Введите стандартное отклонение: "))

    if std_value < 0:
        print("Ошибка: стандартное отклонение не может быть отрицательным.")
        return

    # Экстремальные значения (5 std) — более строгая проверка, делаем приоритетной
    extreme = (max_value > mean_value + 5 * std_value) or (min_value < mean_value - 5 * std_value)
    outliers = (max_value > mean_value + 3 * std_value) or (min_value < mean_value - 3 * std_value)

    if extreme:
        print("В ваших данных имеются экстремальные значения и требуют предобработки")
    elif outliers:
        print("В ваших данных имеются выбросы и требуют предобработки")
    else:
        print("Ваши данные пригодны для анализа")


